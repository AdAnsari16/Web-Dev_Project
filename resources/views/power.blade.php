<h2>Style</h2>

<p class="text-justify">
	One of the biggest hurdles to starting yoga is figuring out what style of yoga you want to try. It's often confusing for beginners because the class names and options are so broad. While almost all styles use the same physical postures, each has a particular emphasis. This cheat sheet highlights the differences so you can determine which type is most appealing to you.<br>
Aside from classes designated as "beginner yoga," generally speaking, classes labeled as "hatha" are slower-moving, thoughtful practices that focus on basic, beginner-friendly poses.2﻿ Of course, it's important to recognize that the term "hatha" is actually a generic designation for any form of yoga focused on movement. In America, almost every yoga class is, technically, hatha, so make sure you ask the instructor what you can expect before your first class.<br>
