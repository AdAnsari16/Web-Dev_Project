		<h1>POWER YOGA</h1>


	<p class="text-justify">
    The term "power yoga" became popular during the mid-1990s when two American yoga teachers who had studied with Ashtanga guru Sri K. Pattabhi Jois began to make what they had learned more accessible to Western students. They also wanted to move away from the rigid Ashtanga sequence, which is a set series of poses that are always done in the same order.<BR>

  Bryan Kest, based in Los Angeles, and Beryl Bender Birch, based in New York, are most often credited with the nearly simultaneous invention of power yoga on opposite coasts. Both were part of the second generation of American Ashtanga students; Kest originally learned from David Williams and Bender Birth from yoga guru Norman Allen. Williams and Allen were both among Jois's first western students. Kest went on to study with Jois in Mysore, India. Bender Birch, who had previously done Sivananda, Kundalini, and Iyengar yoga, worked with Jois during his trips to the United States in the 1980s.<br>
  Power yoga is a general term used to describe a vigorous, fitness-based approach to vinyasa-style yoga. Though many consider power yoga to be superficial "gym yoga," this style of yoga practice was originally closely modeled on the Ashtanga method.<br>
