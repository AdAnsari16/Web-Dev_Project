<h2>Avoid Power Yoga If:</h2>

<p class="text-justify">
  1. You aren’t in good shape. Power yoga, at a minimum, requires you to be moderately fit—otherwise, the physically intensive asanas (poses) can end up doing more harm than good.<br>
  2. You are pregnant. Certain asanas can potentially cause complications during pregnancy. (Instead, try prenatal yoga classes, which are gentle and designed for pregnant women.)<br>
  3. You suffer from a chronic physical ailment such as diabetes or arthritis<br>


