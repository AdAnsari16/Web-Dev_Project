		<h2>1. Downward Facing Dog Pose (Adho Mukha Svanasana)</h2>

	<p class="text-justify">
		One of the most famous asanas in a hatha practice is downward facing dog pose. Downward facing dog or ado mukha svanasana is main part of a typical vinyasa sun salutation flow, also known as surya namaskar.<br>

Benefits of Downward Facing Dog Pose<br>
Downward facing dog pose increases the blood flow as well as oxygen levels in your brain. Doing downward dog correctly, it helps with opening your chest and shoulders as well as strengthening both your arms and your shoulders. The pose also stretches the back of your legs and your spine.<br>

How to practice Downward Facing Dog Pose<br>
Place your hands and knees on the ground, both hip width apart with your knees right below your hip and your palms underneath your shoulders firmly grounding onto the floor. Spread your fingers so that your middle finger is pointing straight ahead. Keep your neck straight while gazing down to a point in the middle between your hands.

While exhaling, start engaging your core and slowly lift your knees away from the ground. Simultaneously, press your palms even firmer onto the ground so that your torso is lifting and your chest and shoulders start to open.
