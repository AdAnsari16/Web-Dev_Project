<h2>3. Half Lord of the Fishes (Ardha Matsyendrāsana)</h2>

<p class="text-justify">
	Benefits of Half Lord of the Fishes Pose<br>
	Half Lord of the fishes pose, also known as half spinal twist pose, helps with increasing abdominal fire and improves organ function.<br>

	Twists improve spinal flexibility<br>
	Exercises that are focusing on rotations generally improve organ function, especially function of liver and kidneys<br>
	Rotations of the lower spine also increase the abdominal fire – digestion and metabolism improve.<br>
	Twists also help with stress relief by calming the mind and nervous system<br>
	How to practice Half Lord of the Fishes<br>
	To practice half lord of the fishes, sit down on the floor with both of your legs straight in front of you. Bend both of your legs and start with your right side: Slide your left knee under your right leg, your left knee is pointing forward and your left foot is placed next to your right hip.<br>

	With your exhale, use your core to slowly rotate your torso towards your right. Place your right palm behind you on the floor, your left hand takes hold of your right knee or you can simply place your left elbow outside of your right knee, with the palm pointing away from you. Your gaze follows. With every inhale lengthen your spine and get tall in your upper body. With every exhale, see if there is a little bit more space that has been created which allows you to twist even further. Repeat on the other side.


